        <h2>Privacy Policy</h2>
        <p><em>Last updated: 23 May 2026</em></p>

        <h3>1. What Data We Collect</h3>
        <p>We collect the following personal information:</p>
        <ul>
          <li><strong>Account data:</strong> name, email address, username, password (hashed)</li>
          <li><strong>Membership data:</strong> name, address, phone number, emergency contact, medical/qualification dates, gliding credentials (GNZ number, etc.)</li>
          <li><strong>Flight records:</strong> dates, times, aircraft, pilots, launch types, billing information</li>
          <li><strong>Social login data:</strong> if you sign in with Google or Facebook, we collect your name and email address from that provider</li>
        </ul>

        <h3>2. How We Use Your Data</h3>
        <ul>
          <li>To operate the gliding club operations system (flight logging, billing, roster management)</li>
          <li>To authenticate you via password or social login (Google/Facebook)</li>
          <li>To send operational emails (magic link login, broadcast messages from the club)</li>
          <li>To generate reports for club management and NZ Civil Aviation requirements</li>
        </ul>

        <h3>3. Data Retention</h3>
        <p>We retain your data for as long as your account is active. Flight records are retained permanently for historical and aviation compliance purposes. You may request deletion of your account and associated personal data at any time (see section 6).</p>

        <h3>4. Third-Party Sharing</h3>
        <p>We do not sell, trade, or share your personal data with third parties except:</p>
        <ul>
          <li><strong>Google / Facebook:</strong> when you choose to sign in via OAuth, basic profile info (name, email) is obtained from the provider per your consent</li>
          <li><strong>Gliding New Zealand (GNZ):</strong> limited membership data is shared as required for national gliding administration</li>
          <li><strong>Email delivery:</strong> operational emails are sent via the club's mail server</li>
        </ul>

        <h3>5. Data Security</h3>
        <p>We use industry-standard security measures including password hashing (not stored in plain text), session-based authentication, and server-side access controls. However, no method of transmission over the Internet is 100% secure.</p>

        <h3>6. Your Rights</h3>
        <p>You have the right to:</p>
        <ul>
          <li>Access the personal data we hold about you</li>
          <li>Request correction of inaccurate data</li>
          <li>Request deletion of your account and personal data</li>
          <li>Withdraw consent for social login at any time</li>
        </ul>
        <p>To exercise these rights, contact the club operations manager or use our <a href="/data-deletion">Data Deletion page</a>.</p>

        <h3>7. Social Login Providers</h3>
        <p><strong>Google:</strong> When you sign in with Google, we receive your name and email address from Google's userinfo API. Google's own privacy policy applies to the initial authentication step. You can revoke Google's access to your account at any time via your <a href="https://myaccount.google.com/permissions" target="_blank">Google Account permissions</a>.</p>
        <p><strong>Facebook:</strong> When you sign in with Facebook, we receive your name and email address from Facebook's Graph API. Facebook's own privacy policy applies to the initial authentication step. You can revoke Facebook's access to your account at any time via your <a href="https://www.facebook.com/settings?tab=applications" target="_blank">Facebook Apps and Websites</a> settings.</p>

        <h3>8. Contact</h3>
        <p>For privacy-related inquiries, contact the club operations manager at the Wellington Gliding Club.</p>
